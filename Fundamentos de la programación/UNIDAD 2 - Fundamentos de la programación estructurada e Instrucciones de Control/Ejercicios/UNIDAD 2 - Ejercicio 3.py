# Escriba un programa que solicite al usuario ingresar dos números decimales, 1 y b, y luego utilizando dichos números imprima: la suma, la resta,, la multiplicación la división y el módulo de ambos.
a = float(input("Ingrese primer decimal: "))
b = float(input("Ingrese segundo decimal: "))

print("La suma es:",a+b)
print("La resta es:",a-b)
print("La multiplicación es:",a*b)
print("La división es:",a/b)
print("El modulo es",a%b)