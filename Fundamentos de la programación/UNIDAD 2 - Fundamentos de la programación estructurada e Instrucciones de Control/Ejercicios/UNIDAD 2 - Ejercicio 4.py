# Escriba un programa que solicite al usuario ingresar dos números enteros, a y b, y luegoi imprima un mensaje que diga "iguales" si ambos son iguales o "Diferentes" si ambos son diferentes.

a = int(input("Ingrese el primer número entero: "))
b = int(input("Ingrese el segundo número entero: "))

if a == b :
    print("Son iguales")
else:
    print("Son diferentes")